local INTERVAL_SECONDS = 8

local mountSounds = GlobalEvent("MountSounds")

function mountSounds.onThink(interval)
	if not configManager.getBoolean(configKeys.MOUNT_SOUNDS) then
		return true
	end

	for _, player in ipairs(Game.getPlayers()) do
		local outfit = player:getOutfit()
		local mountId = outfit.lookMount
		if mountId and mountId > 0 then
			local sounds = mounts[mountId]
			if sounds and math.random(100) >= 50 then
				player:say(sounds[math.random(#sounds)], TALKTYPE_MONSTER_SAY)
			end
		end
	end
	return true
end

mountSounds:interval(INTERVAL_SECONDS * 1000)
mountSounds:register()
